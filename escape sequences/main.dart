void main() {
  //x++
  //++x
  int x = 5;
  print(x++); // Post-increment: Prints the current value of x (5) and then increments it
  print(x); // Prints the updated value of x after the post-increment (6)
  print(++x); // Pre-increment: Increments x first (7) and then prints the updated value
}
