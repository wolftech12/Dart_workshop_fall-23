void main() {
  // * \n
  // * \t
  // * \r
  // * \

  print("Hello world"); //print Hello world

  print(
      "Hello\nworld"); // In this case, the text being output is "Hello" followed by a ***newline**** character ("\n") and then "world".

  print(
      "\tHello world"); // The "\t" is a special escape sequence that represents a horizontal ***tab***

  print(
      "Hello\rworld"); // the text being printed is "Hello" followed by a carriage return character ("\r") and then "world". The "\r" is a special escape sequence that represents a carriage return

   print("Hello\world"); //is used as an escape character.
}
